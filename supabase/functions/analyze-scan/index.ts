import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { type, input } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const systemPrompt = `You are PhishGuard AI, an expert cybersecurity analyst specializing in detecting phishing, scams, and fraud. Analyze the user's input and return ONLY valid JSON (no markdown, no code blocks).

For type "${type}", analyze the provided ${type === 'call' ? 'call transcript' : type === 'sms' ? 'SMS/email message' : type === 'link' ? 'URL/link' : 'website URL'}.

Return this exact JSON structure:
{
  "verdict": "safe" | "suspicious" | "dangerous",
  "confidence": <number 0-100>,
  "title": "<emoji + short title>",
  "explanation": "<2-3 sentence explanation>",
  "keywords": ["<detected patterns>"],
  "checks": [
    {"label": "<check name>", "status": "pass" | "warn" | "fail", "detail": "<brief detail>"}
  ],
  "voiceEN": "<simple English explanation for voice, 5th grade reading level>",
  "voiceHI": "<Hindi explanation in romanized Hindi>"
}

Include exactly 6 checks relevant to the scan type. Be thorough and accurate. For URLs, check for HTTPS, domain reputation, URL structure, phishing patterns, brand impersonation, and redirect risks. For text, check for personal info requests, urgency tactics, financial requests, threat language, too-good-to-be-true offers, and grammar quality.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: systemPrompt },
          { role: "user", content: `Analyze this ${type}: ${input}` },
        ],
        max_tokens: 1200,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limited. Please wait a moment and try again." }), {
          status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits depleted. The app will use local analysis instead." }), {
          status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(JSON.stringify({ error: "AI analysis failed" }), {
        status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    const data = await response.json();
    const content = data.choices?.[0]?.message?.content || "";
    
    // Parse JSON from response, handling potential markdown wrapping
    let parsed;
    try {
      const jsonMatch = content.match(/\{[\s\S]*\}/);
      parsed = JSON.parse(jsonMatch ? jsonMatch[0] : content);
    } catch {
      console.error("Failed to parse AI response:", content);
      return new Response(JSON.stringify({ error: "Failed to parse AI response" }), {
        status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(JSON.stringify(parsed), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  } catch (e) {
    console.error("analyze-scan error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
